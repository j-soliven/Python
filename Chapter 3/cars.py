cars = ['bmw', 'audi', 'toyota', 'honda']
cars.sort()
print(cars)
cars.sort(reverse = True)
print(cars)

print("Here is the original list:")
print(cars)

print("\nHere is the sorted list:")
print(sorted(cars))

print("\nHere is the original list again:")
print(cars)

cars.reverse()
print(cars)

len(cars)
print(len(cars))
